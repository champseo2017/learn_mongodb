/* 
ปรับแต่งผลการสืบค้นด็อกคิวเมนต์ (Query Modifiers)

การจำกัดจำนวน
เป็นการใช้เมธอด limit() ต่อท้ายการสืบค้นด็อกคิวเมนต์เพื่อจำกัดรายการที่เลือก

รูปแบบ
db.collection_name.find().limit(number)

*/

// เช่น แสดงด็อกคิวเมนต์ใน คอลเล็กชัน writer เพียง 2 รายการ
db.writer.find().limit(2);

// การใช้เมธอด sort() และ limit() ร่วมกัน เช่น เราจะใช้ sort({age: -1}) เรียงลำดับอายุจากมากไปหาน้อยและจำกัดให้แสดงเฉพาะ 2 รายการด้วย limit(2)
db.writer.find().sort({ age: -1 }).limit(2);
